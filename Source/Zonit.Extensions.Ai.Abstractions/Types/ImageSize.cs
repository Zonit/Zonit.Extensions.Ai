namespace Zonit.Extensions.Ai;

/// <summary>
/// Image size/dimensions.
/// </summary>
public enum ImageSize
{
    /// <summary>
    /// Square format (1024x1024).
    /// </summary>
    Square,

    /// <summary>
    /// Portrait format (1024x1792).
    /// </summary>
    Portrait,

    /// <summary>
    /// Landscape format (1792x1024).
    /// </summary>
    Landscape,

    /// <summary>
    /// Small square (512x512).
    /// </summary>
    Small,

    /// <summary>
    /// Large square (1536x1536).
    /// </summary>
    Large
}
